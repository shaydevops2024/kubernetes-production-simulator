"""ObserveApp — Observability Demo Application

A FastAPI application that simulates a SaaS analytics platform.
It is instrumented with:
  - Prometheus metrics (HTTP request count/latency + custom business metrics)
  - OpenTelemetry distributed tracing (exports to Jaeger/Tempo via OTLP)
  - Structured JSON logging (compatible with Loki/Promtail)

Your job is NOT to write this application — it's already done.
Your job is to instrument and observe it using the tools in this project.
"""

