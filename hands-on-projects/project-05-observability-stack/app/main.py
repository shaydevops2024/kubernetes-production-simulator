async def toggle_slowdown():
    _sim["slow_mode"] = not _sim["slow_mode"]
    state = "enabled" if _sim["slow_mode"] else "disabled"
    log.warning(f"slowdown simulation {state}")
    return {"slow_mode": _sim["slow_mode"], "message": f"Slow mode {state}"}


@app.post("/api/simulate/traffic")
async def toggle_traffic():
    _sim["high_traffic"] = not _sim["high_traffic"]
    state = "enabled" if _sim["high_traffic"] else "disabled"
    log.info(f"high traffic simulation {state}")
